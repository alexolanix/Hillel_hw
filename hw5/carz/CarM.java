package carz;

public class CarM {
    int id;
    String make;
    String model;
    int manufacturingYear;
    String color;
    int price;
    int registrationNumber;

    public CarM(int id, String make, String model, int manufacturingYear,
                String color, int price, int registrationNumber) {
        this.id = id;
        this.make = make;
        this.model = model;
        this.manufacturingYear = manufacturingYear;
        this.color = color;
        this.price = price;
        this.registrationNumber = registrationNumber;
    }


}
